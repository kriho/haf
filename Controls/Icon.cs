  public class Icon: Image {

    private static ResourceDictionary dictionary;

    static Icon() {
      if (Application.Current.Resources.Contains("Icon" + Enum.GetValues(typeof(IconKey)).Cast<IconKey>().Last().ToString())) {
        Icon.dictionary = Application.Current.Resources;
      } else {
        Icon.dictionary = new ResourceDictionary();
        Icon.dictionary.Source = new Uri("/HAF;component/Resources/Icons.xaml", UriKind.RelativeOrAbsolute);
      }
    }

    public static readonly DependencyProperty KeyProperty = DependencyProperty.Register("Key", typeof(IconKey), typeof(Icon), new PropertyMetadata(new PropertyChangedCallback(OnKeyChanged)));

    public IconKey Key {
      get { return (IconKey)this.GetValue(KeyProperty); }
      set { this.SetValue(KeyProperty, value); }
    }

    public static readonly DependencyProperty ForegroundProperty = DependencyProperty.Register("Foreground", typeof(Brush), typeof(Icon), new PropertyMetadata(new PropertyChangedCallback(OnForegroundChanged)));

    public Brush Foreground {
      get { return (Brush)this.GetValue(ForegroundProperty); }
      set { this.SetValue(ForegroundProperty, value); }
    }

    private static void OnKeyChanged(DependencyObject obj, DependencyPropertyChangedEventArgs args) {
      if (obj is Icon icon) {
        icon.Source = new DrawingImage(((DrawingGroup)Icon.dictionary["Icon" + args.NewValue.ToString()]).Clone());
        icon.ApplyBrush(icon.Foreground);
      }
    }

    private static void OnForegroundChanged(DependencyObject obj, DependencyPropertyChangedEventArgs args) {
      if (obj is Icon icon) {
        icon.ApplyBrush((Brush)args.NewValue);
      }
    }

    private IEnumerable<GeometryDrawing> GetDrawings() {
      return this.Source is DrawingImage drawingImage ? this.GetDrawings(drawingImage) : Enumerable.Empty<GeometryDrawing>();
    }

    private IEnumerable<GeometryDrawing> GetDrawings(DrawingImage drawingImage) {
      if (drawingImage?.Drawing is DrawingGroup drawingGroup) {
        return this.GetDrawings(drawingGroup);
      } else if (drawingImage?.Drawing is GeometryDrawing geometryDrawing) {
        return new List<GeometryDrawing>() {
          geometryDrawing
        };
      }
      return Enumerable.Empty<GeometryDrawing>();
    }

    private List<GeometryDrawing> GetDrawings(DrawingGroup drawingGroup) {
      var drawings = new List<GeometryDrawing>();
      if (drawingGroup == null) {
        return drawings;
      }
      foreach (var drawing in drawingGroup.Children) {
        if (drawing is DrawingGroup dg) {
          drawings.AddRange(this.GetDrawings(dg));
        } else if (drawing is GeometryDrawing gd) {
          drawings.Add(gd);
        }
      }
      return drawings;
    }

    private void ApplyBrush(Brush brush) {
      foreach (var drawing in this.GetDrawings()) {
        this.ApplyColor(drawing, brush);
      }
    }

    private void ApplyColor(GeometryDrawing drawing, Brush brush) {
      drawing.Brush = brush;
    }
  }

